package me.mykindos.betterpvp.core.npc.model;

public enum PlayerListVisibility {
    /**
     * The custom name is visible to all players.
     */
    CUSTOM_NAME_VISIBLE,

    /**
     * The profile name is visible to all players. The custom name is not visible.
     */
    PROFILE_NAME_VISIBLE,

    /**
     * The NPC name is invisible to all players.
     */
    INVISIBLE
}
