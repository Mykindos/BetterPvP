package me.mykindos.betterpvp.core.chat.channels;

public enum ChatChannel {

    /**
     * For staff to send messages to all server
     */
    GLOBAL,
    /**
     * Messages are sent to all players on the current server
     */
    SERVER,
    /**
     * Messages are sent to all players in the same clan
     */
    CLAN,
    /**
     * Messages are sent to all players in the same alliance
     */
    ALLIANCE,
    /**
     * Messages are sent to all players in the same team
     */
    TEAM,
    /**
     * Messages are sent to all players spectating
     */
    SPECTATOR,
    /**
     * Messages are sent to all staff members
     */
    STAFF

}
